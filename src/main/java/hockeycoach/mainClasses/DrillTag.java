package hockeycoach.mainClasses;

public class DrillTag {
    int tagID;
    String drillTag;

    public DrillTag(){}

    public int getTagID() {
        return tagID;
    }

    public void setTagID(int tagID) {
        this.tagID = tagID;
    }

    public String getDrillTag() {
        return drillTag;
    }

    public void setDrillTag(String drillTag) {
        this.drillTag = drillTag;
    }
}
