package hockeycoach.mainClasses.Drills;

public class DrillParticipation {
    public int ID;
    public String drillParticipation;

    public DrillParticipation(){}

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getDrillParticipation() {
        return drillParticipation;
    }

    public void setDrillParticipation(String drillParticipation) {
        this.drillParticipation = drillParticipation;
    }
}
